`timescale 1ns / 1ps

module lab5_tb(
    );
    
    wire [6:0] out;
    reg [5:0] a, b;
    reg [1:0] sw_sel;
    
    wire [23:0] op_code;
    
    assign op_code = sw_sel == 2'b10 ? "ADD" : sw_sel == 2'b01 ? "SUB" : "MUL";
    
    lab5 uut(
    .SW ({sw_sel, 2'b00, b, a}),
    .LED (out[6:0])
    );
    
    initial begin
        $monitor("OP_CODE = %s, A_BUS = %h, B_BUS = %h, OUT = %h", op_code, a[5:0], b[5:0], out[6:0]);
        //ADD
        sw_sel[1:0] = 2'b10;
        a[5:0] = 6'b000001;
        b[5:0] = 6'b000001;
        #10;
        a[5:0] = 6'b000111;
        b[5:0] = 6'b000111;
        #10;
        a[5:0] = 6'b011111;
        b[5:0] = 6'b011111;
        #10;
        a[5:0] = 6'b111111;
        b[5:0] = 6'b111111;
        #10;
        
        //SUB
        sw_sel[1:0] = 2'b01;
        a[5:0] = 6'b000001;
        b[5:0] = 6'b000000;
        #10;
        a[5:0] = 6'b000011;
        b[5:0] = 6'b000001;
        #10;
        a[5:0] = 6'b000100;
        b[5:0] = 6'b000001;
        #10;
        a[5:0] = 6'b111111;
        b[5:0] = 6'b111111;
        #10;
        
        //MUL
        sw_sel[1:0] = 2'b11;
        a[5:0] = 6'b000001;
        b[5:0] = 6'b000001;
        #10;
        a[5:0] = 6'b000001;
        b[5:0] = 6'b000011;
        #10;
        a[5:0] = 6'b000001;
        b[5:0] = 6'b010100;
        #10;
        a[5:0] = 6'b000001;
        b[5:0] = 6'b000111;
        #10;
        
    end
endmodule
